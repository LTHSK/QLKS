    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        try 
        {
            serviceDetial = new ServiceDetail();
            od = new OrderDetail();
            if(cbbRoom.getSelectedIndex()==0 || quantity.equals("")|| cbbService.getSelectedIndex()==0)
            {
                JOptionPane.showMessageDialog(this, "Cần phải chọn và nhập đầy đủ thông tin!");
            }
            else
            {
                if(Integer.parseInt(quantity.getText()) <= 0)
                {
                    JOptionPane.showMessageDialog(this, "Số lượng phải lớn hơn 0!");
                }
                else
                {
                    serviceDetial.setQuantity(Integer.parseInt(quantity.getText()));
                    Service s =  serviceD.findServiceID(cbbService.getSelectedItem().toString());
                    Double p = s.getPrice();
                    int q = Integer.parseInt(quantity.getText());
                    Double tt = p * q;
                    serviceDetial.setTt(tt);
                }
            }
            } catch (Exception e) {
            }
    }//GEN-LAST:event_btnAddActionPerformed


