    private void loadDataToCBBNameRoom() {
        for(Room r : roomD.getAllRooms())
        {
            if(r.getRoomStatusType().getRoomStatusTypeID().equals("LTTP002"))
            {
                cbbRoom.addItem(r.getRoomID());
                loadDataToTBLSA(r.getRoomID());
            }
        }
    }

    private void loadDataToCBBService() {
        try {
            for(Service s : serviceD.getAllService())
            {
                cbbService.addItem(s.getServiceName());
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(GD_DichVu.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(GD_DichVu.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void loadDataToTBLS() {
        try {
            modelListService.setRowCount(0);
            for(Service s : serviceD.getAllService())
            {
                Object[] row = {
                    s.getServiceName(),s.getPrice(),s.getInventory()
                };
                modelListService.addRow(row);
            }
            modelListService.fireTableDataChanged();
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(GD_DichVu.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(GD_DichVu.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void loadDataToTBLSA(String id) {
        modelListServiceAdded.setRowCount(0);
        try {
            for(ServiceDetail sd : serviceDetailDAO.getListSericeByRoomID(id))
            {
                Object[] row = {
                    sd.getBookRoom().getRoom().getRoomID(), sd.getService().getServiceName(), sd.getQuantity(), sd.getTt()
                };
            }
//        addCellTable();
        } catch (SQLException ex) {
            Logger.getLogger(GD_DichVu.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(GD_DichVu.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    
